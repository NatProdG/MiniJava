class Fibonacci {
    public static void main(String[] args) {
        System.out.println(new Fibo().fibo());
    }
}

class Fibo {
    public int fibo() {
        int x;
        x = 5;
        if (10 <= 10) {
           ++x;
        }
        else {
            x = 2;
        }
        return x;
    }
}